    public void Sound(int a) throws FileNotFoundException{
        
        String SFX = "";
        
        switch(a){
            
            case 1:
                SFX = "D.wav";
            break;
            
            case 2:
                SFX = "A.wav";
            break;
            
            case 3:
                SFX = "C.wav";
            break;
            
            case 4:
                SFX = "G.wav";
            break;
            
            default:
                SFX = "N.wav";
            break;
        }
        
        try {
    File yourFile = new File(SFX);
    AudioInputStream stream;
    AudioFormat format;
    DataLine.Info info;
    Clip clip;

    stream = AudioSystem.getAudioInputStream(yourFile);
    format = stream.getFormat();
    info = new DataLine.Info(Clip.class, format);
    clip = (Clip) AudioSystem.getLine(info);
    clip.open(stream);
    clip.start();
    }
        catch ( IOException | LineUnavailableException | UnsupportedAudioFileException e) {
            //whatevers
    }
        
    }
    
    
